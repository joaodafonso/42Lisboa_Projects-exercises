#include "libft.h"

int	ft_isprint(char *str)
{
	int	i;

	i = 0;
	while ((str[i] >= 32 && str[i] <= 126) || str[i] == '\0')
	{
		if (str[i] == '\0')
		{
			return (1);
		}
		i++;
	}
	return (0);
}
